package a00_batch12;

public class switch_task {

	public static void main(String[] args) {

		// W.A.P. check char is a,e,i,o,u is vowel otherwise consonant.
		char ch='a';
		switch(ch) {
		case 'a': 
			System.out.println("vowel");
			break;
		case 'e':	
			System.out.println("vowel");
		case 'i':	
			System.out.println("vowel");
		case 'o':	
			System.out.println("vowel");
		case 'u':	
			System.out.println("vowel");
		default:	
			System.out.println("consonant");
		}
	}

}
