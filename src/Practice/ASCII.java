package Practice;

public class ASCII {
	
	// American Standard for Information Interchange
	
	char c='G';               // For Small character start from 97   i.e. a=97
	char d='A';               // for Capital character start from 65  i.e. A= 65
	int e=c;
	int f=d;

	
	
	public static void main(String[] args) {
		
		ASCII p=new ASCII();
		
		System.out.println(p.e);
		System.out.println(p.f);

	}

}


